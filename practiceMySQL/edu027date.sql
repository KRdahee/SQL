#특정 날짜
SELECT DATE('2022-05-11 14:27:30'), MONTH('2022-05-11 14:27:30'), DAY('2022-05-11 14:27:30');
SELECT DATE(NOW()), MONTH(NOW()), DAY(NOW());

#특정 시간
SELECT HOUR('14:27:30'), MINUTE('14:27:30'), SECOND('14:27:30');
SELECT HOUR(NOW()), MINUTE(NOW()), SECOND(NOW()); 

#날짜/시간 계산
SELECT ADDDATE('2022-05-07', INTERVAL 31 DAY), ADDDATE('2022-05-07', INTERVAL 1 MONTH);
SELECT DATEDIFF('2022-11-07', CURDATE()), TIMEDIFF('23:59:59', CURTIME());

#날짜 형식 변경
SELECT DATE_FORMAT('2022-08-19 19:23:11', '%D %y %a %d %m %b %j');